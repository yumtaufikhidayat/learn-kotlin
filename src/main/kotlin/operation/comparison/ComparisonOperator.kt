package operation.comparison

fun main() {

    //Comparison
    val a = 100
    val b = 200
    val result = a > b

    //For result comparison
    println(result)

    //Comparison using number data type
    println(100 >= 200)
    println(100 <= 200)
    println(100 == 200)
    println(100 == 100)
    println(100 != 100)

    //Comparison using string data type
    println(a >= b)
    println(a <= b)
    println(a == b)
    println(a == a)
    println(a != a)

}